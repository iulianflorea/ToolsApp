@Injectable({ providedIn: 'root' })
export class TranslationService {
  readonly currentLang = signal<Lang>('ro');

  constructor() {
    const saved = localStorage.getItem('lang') as Lang | null;
    if (saved && (saved === 'ro' || saved === 'en' || saved === 'fr')) {
      this.currentLang.set(saved);
    }
  }

  setLang(lang: Lang): void {
    this.currentLang.set(lang);
    localStorage.setItem('lang', lang);
  }

  t(key: string): string {
    return T[this.currentLang()][key] ?? key;
  }

  tf(key: string, ...args: (string | number)[]): string {
    let result = this.t(key);
    args.forEach((arg, i) => { result = result.replace(`{${i}}`, String(arg)); });
    return result;
  }
}
